package kr.or.plzdvl.support.vo;

public class SupportVO {
	private String sup_num; // 지원번호
	private String sup_time; // 지원시간
	private String mem_id; // 지원한 회원
	private String pro_num; // 프로젝트 번호
	private String mem_nm; // 지원한 회원 이름
	
	public String getMem_nm() {
		return mem_nm;
	}
	public void setMem_nm(String mem_nm) {
		this.mem_nm = mem_nm;
	}
	public String getSup_num() {
		return sup_num;
	}
	public void setSup_num(String sup_num) {
		this.sup_num = sup_num;
	}
	public String getSup_time() {
		return sup_time;
	}
	public void setSup_time(String sup_time) {
		this.sup_time = sup_time;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getPro_num() {
		return pro_num;
	}
	public void setPro_num(String pro_num) {
		this.pro_num = pro_num;
	}
	
	
}
