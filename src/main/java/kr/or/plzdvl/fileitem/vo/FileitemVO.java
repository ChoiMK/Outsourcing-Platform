package kr.or.plzdvl.fileitem.vo;

public class FileitemVO {
	private String file_num; // 파일 번호 
	private String file_size; // 파일 크기
	private String file_cod; // 파일 상태
	private String file_upl_date; // 업로드일
	private String file_con_type; // 내용형태
	private String file_upl_nm; // 업로드 파일명
	private String file_save_nm; // 저장 파일명
	private String file_bod_num; // 파일위치
	
	public String getFile_num() {
		return file_num;
	}
	public void setFile_num(String file_num) {
		this.file_num = file_num;
	}
	public String getFile_size() {
		return file_size;
	}
	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}
	public String getFile_cod() {
		return file_cod;
	}
	public void setFile_cod(String file_cod) {
		this.file_cod = file_cod;
	}
	public String getFile_upl_date() {
		return file_upl_date;
	}
	public void setFile_upl_date(String file_upl_date) {
		this.file_upl_date = file_upl_date;
	}
	public String getFile_con_type() {
		return file_con_type;
	}
	public void setFile_con_type(String file_con_type) {
		this.file_con_type = file_con_type;
	}
	public String getFile_upl_nm() {
		return file_upl_nm;
	}
	public void setFile_upl_nm(String file_upl_nm) {
		this.file_upl_nm = file_upl_nm;
	}
	public String getFile_save_nm() {
		return file_save_nm;
	}
	public void setFile_save_nm(String file_save_nm) {
		this.file_save_nm = file_save_nm;
	}
	public String getFile_bod_num() {
		return file_bod_num;
	}
	public void setFile_bod_num(String file_bod_num) {
		this.file_bod_num = file_bod_num;
	}
	
	
}
