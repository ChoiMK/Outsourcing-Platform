package kr.or.plzdvl.portfolio.vo;

import java.util.List;

import kr.or.plzdvl.fileitem.vo.FileitemVO;

public class PortfolioVO {

	private String po_num; // 포트폴리오 번호
	private String po_tit; // 포트폴리오 제목
	private String po_prog_term; // 진행 기간
	private String po_part; // 참여율
	private String po_con; // 내용
	private String mem_id; // 작성자
	private List<FileitemVO> attachFileItemList; // 사진
	
	public String getPo_num() {
		return po_num;
	}
	public void setPo_num(String po_num) {
		this.po_num = po_num;
	}
	public String getPo_tit() {
		return po_tit;
	}
	public void setPo_tit(String po_tit) {
		this.po_tit = po_tit;
	}
	public String getPo_prog_term() {
		return po_prog_term;
	}
	public void setPo_prog_term(String po_prog_term) {
		this.po_prog_term = po_prog_term;
	}
	public String getPo_part() {
		return po_part;
	}
	public void setPo_part(String po_part) {
		this.po_part = po_part;
	}
	public String getPo_con() {
		return po_con;
	}
	public void setPo_con(String po_con) {
		this.po_con = po_con;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public List<FileitemVO> getAttachFileItemList() {
		return attachFileItemList;
	}
	public void setAttachFileItemList(List<FileitemVO> attachFileItemList) {
		this.attachFileItemList = attachFileItemList;
	}
}
