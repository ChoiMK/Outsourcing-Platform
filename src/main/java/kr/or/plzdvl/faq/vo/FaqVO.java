package kr.or.plzdvl.faq.vo;

public class FaqVO {
	
	private String faq_num;
	private String faq_tit;
	private String faq_con;
	private String faq_inst_date;
	private String mem_id;
	private String faq_wht;
	public String getFaq_num() {
		return faq_num;
	}
	public void setFaq_num(String faq_num) {
		this.faq_num = faq_num;
	}
	public String getFaq_tit() {
		return faq_tit;
	}
	public void setFaq_tit(String faq_tit) {
		this.faq_tit = faq_tit;
	}
	public String getFaq_con() {
		return faq_con;
	}
	public void setFaq_con(String faq_con) {
		this.faq_con = faq_con;
	}
	public String getFaq_inst_date() {
		return faq_inst_date;
	}
	public void setFaq_inst_date(String faq_inst_date) {
		this.faq_inst_date = faq_inst_date;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getFaq_wht() {
		return faq_wht;
	}
	public void setFaq_wht(String faq_wht) {
		this.faq_wht = faq_wht;
	}
	
	
}
