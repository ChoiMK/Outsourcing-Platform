package kr.or.plzdvl.tecinfo.vo;

public class TecinfoVO {
	private String tec_num;
	private String tec_info_lan;
	private String tec_rat;
	private String tec_car;
	private String mem_id;
	
	public String getTec_num() {
		return tec_num;
	}
	public void setTec_num(String tec_num) {
		this.tec_num = tec_num;
	}
	public String getTec_info_lan() {
		return tec_info_lan;
	}
	public void setTec_info_lan(String tec_info_lan) {
		this.tec_info_lan = tec_info_lan;
	}
	public String getTec_rat() {
		return tec_rat;
	}
	public void setTec_rat(String tec_rat) {
		this.tec_rat = tec_rat;
	}
	public String getTec_car() {
		return tec_car;
	}
	public void setTec_car(String tec_car) {
		this.tec_car = tec_car;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
	
}
